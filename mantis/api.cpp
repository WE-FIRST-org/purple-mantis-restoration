#include <stdio.h>
#include <WiFi.h>
#include "drive.h"
#include "shooter.h"

#define WIFI_SSID "purpol mantis"
#define WIFI_PASS "pmpmpmpm"

WiFiServer server(8000);
Drive drive;
Shooter shooter;


void APISetup() {
    drive.setup(4, 16, 5, 17); // left, left, right, right
    shooter.setup(18, 19, 21);
    
    
    WiFi.mode(WIFI_AP);
    WiFi.softAP(WIFI_SSID, WIFI_PASS); 
    
    #ifndef SERIALBEGIN
    #define SERIALBEGIN 
    Serial.begin(9600);
    #endif
    IPAddress IP = WiFi.softAPIP();
    
    Serial.print("AP IP address: ");
    Serial.println(IP);
    
    server.begin();
}

#ifndef BUFFERSIZE
#define BUFFERSIZE 100
#endif 

char buffer[BUFFERSIZE];
int pos = 0;

int checkCategory(int* caret) {
    std::string cat;
    for(;;) {
        (*caret)++;
        if(buffer[*caret] == '"') break;
        cat += buffer[*caret];
    }
    /**
    * this could be better with a hash map
    * theres only 5 values tho so i think its good enough
    */
    if(cat.compare("speed") == 0) return 0;
    if(cat.compare("turn") == 0) return 1;
    if(cat.compare("aim") == 0) return 2;
    if(cat.compare("shoot") == 0) return 3;
    if(cat.compare("shtspeed") == 0) return 4;
    return -1;
}

int values[5];

void processBuffer() {
    int section = -1;
    int sign = 1;
    for(int caret = 0; caret < pos; caret++) { 
        if(buffer[caret] == '"') {		// determine category
			section = (checkCategory(&caret));
			values[section] = 0;
			sign = 1;
		}
		if(buffer[caret] == 46) section = -1;		// if floating point, just ignore everything after
		if(buffer[caret] == 45 && section != -1) sign = -1;
		if(buffer[caret] > 47 && buffer[caret] < 58 && section != -1) {	// store num
			values[section] *= 10;
			if(sign > 0){
				values[section] += buffer[caret] - 48;
			} else {
				values[section] -= buffer[caret] - 48;
			}
		}
    }
}

void runBot() {
    double speed = values[0];
    speed /= 100;
    double turning = values[1];
    turning /= 100;
    drive.arcade(speed, turning);

    shooter.setSpeed(values[4]);

    if(values[3] == 1) shooter.shoot();
    else shooter.unshoot();
}

void commsLost() {
    drive.arcade(0,0);
    shooter.setSpeed(0);
    shooter.unshoot();
    return;
}

unsigned long lastRead;

void APIRead(void * params) {
    while(true) {
        WiFiClient client = server.available();
        if (client) {
            Serial.println("uhh");
            lastRead = millis();
            int reading = 0;
            while (client.connected()) {

                if(millis() - lastRead > 100) commsLost();
                
                if (client.available()) {      
                    lastRead = millis();
                    char c = client.read();

                    if(c == '{') {
                        reading = 1;
                        pos = 0;
                    } 

                    if(reading > 0) {
                        buffer[pos] = c;
                        pos++;
                        pos %= BUFFERSIZE;
                    }
                    if(c == '}') {
                        Serial.println("received");
                        reading = 0;
                        processBuffer();
                        runBot();
                        client.flush();
                        client.println("HTTP/1.1 200 OK");
                        client.println("Content-type:text/html");
                        client.println("Connection: close");
                        client.println("\r\n");
                        break;
                        //Serial.println(values[0]);
                    }
                }
            }

            client.stop();
        }
    }
}


